const emailSlug = (name: string) =>
    name
        .toLowerCase()
        .replace(/^(dr|eng|ar|atty|prof)\.\s+/u, '')
        .replace(/[^a-z0-9]+/gu, '.')
        .replace(/^\.+|\.+$/gu, '');

const authors = (...names: string[]): RepositoryAuthor[] =>
    names.map((name) => ({
        name,
        email: `${emailSlug(name)}@rikms.example`,
    }));

const file = (
    name: string,
    uploadedAt: string,
    size = '4.8 MB',
    pages = 72,
): RepositoryFileInfo => ({
    name,
    size,
    uploadedAt,
    type: 'PDF Document',
    pages,
});

