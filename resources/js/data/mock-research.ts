const categoryTag = (label: string): ResearchTag => ({
    label,
    color: '#f3f4f6',
    type: 'category',
});

const sdgTag = (label: string): ResearchTag => ({
    label,
    color: sdgColors[label] ?? '#1e3a8a',
    type: 'sdg',
});

const tags = (sdgs: string[], category: string) => [
    ...sdgs.map(sdgTag),
    categoryTag(category),
];

